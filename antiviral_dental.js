+ "* [FDA approves Remdesivir on 2020-10-22](https://www.fda.gov/news-events/press-announcements/fda-approves-first-treatment-covid-19)\n"
+ "* Indication\n"
+ "	* Treatment of COVID-19 in hospitalized adult and pediatric patients\n"
+ "* MoA \n"
+ "	* Originally developed to treat Ebola virus \n"
+ "	* Nucleoside analog of Adenosine\n"
+ "	* It is a prodrug, metabolized by carboxylesterase I to its triphosphate active form\n"
+ "	* Coronaviruses replication requires **RNA-dependent RNA polymerase**. These enzymes can incorporate remdesivir into new RNA strands. Shortly after adding remdesivir, the enzyme stops being able to add more RNA subunits. This halts viral genome replication.\n"
+ "* ADME ([review article](https://www.ncbi.nlm.nih.gov/pmc/articles/PMC8308800/))\n"
+ "	* Administered as a slow i.v. infusion of a loading dose followed by a maintenance dose for 5 or 10 days\n"
+ "	* Eliminated in urine\n"
