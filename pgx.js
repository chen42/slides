+ "## PGx: What are in the names?\n"
+ "\n"
+ "- **Pharmacogenetics**\n"
+ "\n"
+ "focuses on the study of how variations in a single gene or a few genes affect an individual's response to a particular drug. It examines specific genetic variations, such as single nucleotide polymorphisms (**SNPs**), that influence drug metabolism, efficacy, and the likelihood of adverse reactions. An example would be the study of how variations in the CYP2C9 gene affect the metabolism of warfarin, a commonly used anticoagulant.\n"
+ "\n"
+ "- **Pharmacogenomics**\n"
+ "\n"
+ "often used interchangeably with pharmacogenetics. Pharmacogenetics is more limited to single gene-drug interactions, while pharmacogenomics refers to polygenic drug response and encompasses transcriptomics, proteomics, and metabolomics.\n"
