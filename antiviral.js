+ "## Acyclovir\n"
+ "\n"
+ "### [Valacyclovir](https://www.clinicalkey.com/#!/content/drug_monograph/6-s2.0-636) (L-valyl ester prodrug of acyclovir)\n"
+ "\n"
+ "- <a href=\"#/sd\">Mechanism of action</a>\n"
+ "  - <span id=\"drug\"> acyclovir </span> is phosphorylated intracellularly by viral thymidine kinases (TK), then by cellular kinases to the triphosphate state, which terminates DNA replication when incorported into the viral DNA.\n"
+ "  - Only effective against actively replicating virus. Does not eliminate the viral genome.\n"
+ "- ADME\n"
+ "  - <span id=\"drug\"> acyclovir </span> has low oral bioavailability (10-20%). (i.v. or topical)\n"
+ "  - <span id=\"drug\"> Valacyclovir </span> is hydrolyzed to acyclovir in intestine or liver. Increase bioavailability (3.3 - 5 X)\n"
+ "- Indication:\n"
+ "  - HSV keratitis, herpes labialis, genital herpes, HSV encephalitis\n"
+ "  - VZV\n"
+ "  - CMV-positive bone marrow transplant recipients\n"
+ "- Adverse effects\n"
+ "  - Headache, nausea, diarrhea\n"
+ "  - Following high IV dose: Reversible renal dysfunction (crystalline nephropathy), Neurologic toxicity (tremors, delirium, seizures)\n"
