+ "## Protease Inhibitors\n"
+ "\n"
+ "- Mechanism of Action\n"
+ "  - Selective, competitive inhibitors of HIV proteases\n"
+ "  - Bind reversibly to protease active site\n"
+ "  - Prevent cleavage of polyprotein and block viral maturation\n"
+ "  - Early stages of HIV-1 replication cycle not affected\n"
+ "- Resistance\n"
+ "  - Each drug selects for different mutations in protease gene at level of specific codons\n"
+ "- Indications\n"
+ "  - Treatment of HIV as part of combination therapy\n"
+ "  - Effective in both acutely and chronic HIV-1 infected cells\n"
+ "\n"
+ "---\n"
+ "\n"
+ "## Protease inhibitors\n"
+ "\n"
+ "- ADME\n"
+ "  - Oral absorption varies\n"
+ "  - Bind extensively to plasma proteins\n"
+ "  - Renal excretion is minimal\n"
+ "    - No adjustments needed for renal dysfunctions\n"
+ "  - Metabolized by cytochrome P-450 system\n"
+ "    - Concurrent use of potent P-450 inducer (e.g. <span id=\"drug\"> rifampin </span> ) leads to decreased PI concentration\n"
+ "  - <span style=\"background-color:#ff9\"> <span id=\"drug\"> Ritonavir </span> is a potent inhibitors of CYP3A4</span>\n"
+ "    - used to increase (i.e. \"boost\") plasma concentration of other PIs except <span id=\"drug\"> nelfinavir </span> (CYP2C19)\n"
+ "    - Toxic adverse effects related to drug accumulation due to PI-mediated inhibition of hepatic P450 system\n"
+ "\n"
+ "note: rifampin is a bacterial RNA polymerase inhibitor\n"
+ "\n"
